public static class TokenTypeExtensions
{
    extension(TokenType tokenType)
    {
        public TokenType GetDelimiter()
        {
            return tokenType switch
            {
                TokenType.BlockKw => TokenType.EndblockKw,
                TokenType.DoKw => TokenType.WhiletrueKw,
                TokenType.ElseKw or
                TokenType.IfKw => TokenType.EndifKw,
                TokenType.ForKw or TokenType.ForeachKw => TokenType.NextKw,
                TokenType.FunctionKw => TokenType.EndfunctionKw,
                TokenType.NamespaceKw => TokenType.EndnamespaceKw,
                TokenType.PageKw => TokenType.EndpageKw,
                TokenType.ProcKw => TokenType.EndprocKw,
                TokenType.RepeatKw => TokenType.EndrepeatKw,
                TokenType.SwitchKw => TokenType.EndswitchKw,
                TokenType.WhileKw => TokenType.EndwhileKw,
                _ => tokenType
            };
        }

