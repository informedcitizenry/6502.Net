        public bool IsIntOperator()
        {
            return tokenType switch
            {
                TokenType.Percent or
                TokenType.Shl or 
                TokenType.Shr or 
                TokenType.Ashr or 
                TokenType.BitwiseAnd or 
                TokenType.Caret or 
                TokenType.BitwiseOr => true,
                _ => false
            };
        }

        public bool IsStringLiteral()
        {
            return tokenType switch
            {
                TokenType.AtaScreenStringLiteral or
                TokenType.CbmScreenStringLiteral or
                TokenType.InterpolationStart or 
                TokenType.InterpolationEnd or
                TokenType.StringLiteral or
                TokenType.PetsciiStringLiteral or
                TokenType.Utf8StringLiteral or 
                TokenType.Utf16StringLiteral or 
                TokenType.Utf32StringLiteral => true,
                _ => false
            };
        }

        public bool IsVarAssignment()
        {
            return tokenType switch
            {
                TokenType.ColonEq or 
                TokenType.EquKw or
                TokenType.GlobalKw or
                TokenType.OrEq or 
                TokenType.CaretEq or 
                TokenType.AndEq or 
                TokenType.AshrEq or 
                TokenType.ShrEq or 
                TokenType.ShlEq or 
                TokenType.PlusEq or 
                TokenType.MinusEq or 
                TokenType.StarEq or 
                TokenType.PercentEq or 
                TokenType.SlashEq => true,
                _ => false
            };
        }

        public bool IsAssignment() 
            => tokenType == TokenType.Eq || tokenType.IsVarAssignment();

        private bool IsExpression()
            => tokenType.IsIdent() || 
               tokenType.IsRegister() ||
               tokenType.IsStringLiteral() ||
               tokenType is 
                   TokenType.AltBinLiteral or
                   TokenType.CharLiteral or 
                   TokenType.False or 
                   TokenType.FloatLiteral or 
                   TokenType.IntLiteral or
                   TokenType.True;

        public bool BeginsExpression()
            => tokenType.IsExpression() ||
               tokenType is
                   TokenType.OpenBrace or
                   TokenType.OpenBracket or
                   TokenType.OpenParen;
        
        public bool EndsExpression()
            => tokenType.IsExpression() ||
                tokenType is
                   TokenType.CloseBrace or 
                   TokenType.CloseBracket or 
                   TokenType.CloseParen;
        
        public bool IsIdent() 
            => tokenType == TokenType.Ident || 
               tokenType.IsRegister() || 
               tokenType.IsI86PtrType() ||
               tokenType == TokenType.Ptr;

        public bool Is8087Opcode()
            => tokenType is >= TokenType.F2Xm1 and <= TokenType.Fyl2Xp1;
        
        public bool IsRegister() => tokenType is >= TokenType.A and <= TokenType.Z;

        public bool IsExtraDirective()
            => tokenType is >= TokenType.CaseKw and < TokenType.BincludeKw;
        
        public bool IsFieldType()
            => tokenType is TokenType.ByteKw or 
                TokenType.CharKw or 
                TokenType.DintKw or 
                TokenType.DoubleKw or
                TokenType.DwordKw or  
                TokenType.LintKw or 
                TokenType.LongKw or  
                TokenType.QwordKw or
                TokenType.ShortKw or 
                TokenType.SintKw or 
                TokenType.StringKw or 
                TokenType.TbyteKw or
                TokenType.WordKw;

        public bool IsI86Segment()
            => tokenType is TokenType.Cs or TokenType.Ds or TokenType.Es or TokenType.Ss;

        public bool IsI86Base()
            => tokenType is TokenType.Bx or TokenType.Bp;

        public bool IsI86Index()
            => tokenType is TokenType.Di or TokenType.Si;

        public bool IsI86PtrType()
            => tokenType is TokenType.Byte or 
                TokenType.Dword or 
                TokenType.Qword or
                TokenType.Tbyte or
                TokenType.Word;

        public bool IsStParenReg()
            => tokenType is >= TokenType.StParen0Reg and <= TokenType.StParen7Reg;

